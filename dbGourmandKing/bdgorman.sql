CREATE TABLE CLIENTE(
	NUMCLI		int AUTO_INCREMENT PRIMARY KEY NOT NULL ,
	NOMCLI		varchar(30) not NULL ,		-- nombre
	APECLI		varchar(70) not null,		-- apellido
	DNICLI		char(8) not null,		-- dni	
	FNCLI		date,				-- fecha de nacimiento
	DIRCLI		varchar(50),  			-- direccion
	NUMUBI		char(6)				-- ubigeo direccion	
)
;
CREATE TABLE USUARIO		-- personal
(
	NUMUSU			int AUTO_INCREMENT PRIMARY KEY NOT NULL ,
	NOMUSU			varchar(50),
	USUUSU			char(5),
	PWDUSU			varchar(50),
	DNIUSU			char(8),	
	TELFUSU			VARCHAR(30),
	FNACUSU			date,
	CARGUSU			varchar(30) ,
	UBIUSU			CHAR(6),
	LEVUSU			CHAR(1)		-- niveles de acceso: 1 ADMIN , 2 OPERARIO
)
;
CREATE TABLE EMPLEADO
(
	IDEMP		int AUTO_INCREMENT PRIMARY KEY NOT NULL ,
	NOMEMP		varchar(30) not NULL ,		-- nombre
	APEEMP		varchar(70) not null,		-- apellido
	SEXEMP		char(1) not null,		-- sexo	M o F
	DNIEMP		char(8) not null,		-- dni	
	FNEMP		date,				-- fecha de nacimiento
	DIREMP		varchar(50),  			-- direccion
	NUMUBI		char(6),				-- ubigeo direccion	
	ESTEMP		char(1)
)
;

/*Tabla Menu*/
create table MENU
(
	IDMEN int,
    PREMEN decimal(8,2),
    ENTMEN VARCHAR(80),
    SEGUMEN VARCHAR(80),
    BEBMEN VARCHAR(80),
    IDPED int,
    CONSTRAINT IDMEN_PK PRIMARY KEY (IDMEN)
);

/*Tabla Venta*/
CREATE TABLE VENTA
(
	IDVENT INT auto_increment,
    FECVENT DATE,
    NUMCLI int,
    IDEMP int,
     CONSTRAINT IDVENT_PK PRIMARY KEY (IDVENT)
);

/*Tabla Venta detalle*/
CREATE TABLE VENTA_DETALLE
(
	IDVENDET int auto_increment,
    IDVENT int,
    IDMEN int,
    CANTVENDET INT,
    SUBTOVENT decimal(8,2),
    CONSTRAINT IDVENDET_PK PRIMARY KEY (IDVENDET)
);

CREATE TABLE UBIGEO(
	NUMUBI		char(6) not null primary key,
	DEPUBI		varchar(50),
	PROVUBI		varchar(50),
	DISTUBI		varchar(50)
);

